const fs = require("fs");

function lines(items) {
  return items.map((line, index) => (index === items.length - 1 ? line : `${line}\n`));
}

function setMarkdown(file, index, content) {
  const notebook = JSON.parse(fs.readFileSync(file, "utf8"));
  const cell = notebook.cells[index];
  if (!cell) {
    throw new Error(`${file}: cell ${index} does not exist`);
  }
  if (cell.cell_type !== "markdown") {
    throw new Error(`${file}: cell ${index} is ${cell.cell_type}, not markdown`);
  }
  cell.source = lines(content);
  fs.writeFileSync(file, `${JSON.stringify(notebook, null, 1)}\n`, "utf8");
}

